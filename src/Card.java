public class Card {

    private int cardID;
    private int userID;
    private String card;

    public int getUserID() {
        return userID;
    }
    public void setUserID(int userID) {
        this.userID = userID;
    }
    public int getCardID() {
        return cardID;
    }
    public void setCardID(int cardID) {
        this.cardID = cardID;
    }
    public void setCard(String card) {
        this.card = card;
    }
    public String getCard() {
        return card;
    }


}
